//what is variable in javascript
//A variable is a container for storing data values. In JavaScript, you can declare a variable using var, let, or const keywords.   
let message = "This is a variable in JavaScript"
console.log(message) // Output the value of the variable
const pi = 3.14 // constant variable
console.log("Value of pi:", pi)
// You can change the value of a variable declared with let
message = "Variable value changed!"
console.log(message)
// But you cannot change the value of a constant variable declared with const
// pi = 3.14159 // This will throw an error     
var count = 10 // variable declared with var
console.log("Count:", count)
count = 20 // changing the value of var variable
console.log("Updated Count:", count)    
